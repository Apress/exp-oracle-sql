CREATE TABLE t1
AS
       SELECT 1 c1
         FROM DUAL
   CONNECT BY LEVEL <= 100;

CREATE TABLE t2
AS
       SELECT 1 c2
         FROM DUAL
   CONNECT BY LEVEL <= 100;

SET AUTOTRACE TRACEONLY

SELECT *
  FROM t1, t2
 WHERE t1.c1 = t2.c2;


WITH q1
     AS (SELECT *
           FROM t1 SAMPLE (5) SEED (0))
SELECT *
  FROM q1 t1, t2
 WHERE t1.c1 = t2.c2;

SET AUTOTRACE OFF

DROP TABLE t1;
DROP TABLE t2;